<?php

if (php_sapi_name() === 'cli') {
    define('LS', "\n");
} else {
    define('LS', '<br>');
}

// ===========================================================

$a = 5;     // 0x549846109
$b = $a;    // 0x798485198

$a = 10;    // 0x549846109

echo "a - $a, b - $b".LS;

// ===========================================================

$c = 15;    // 0x815151564
$d = &$c;   // 0x815151564

$c = 25;    // 0x815151564

echo "c - $c, d - $d".LS;
